// selecting options
$(".mode-option").click(function(){
    let box = $(this).parent().parent().parent().attr("id");
    let curr = $(this).attr("data-id");
    let mo = document.getElementById(box).getElementsByClassName("mode-option");
    let downed;
    let button;

    // check if any button is clicked
    function notOnlyOne (){
        let test = 0;
        for (let x of mo){
            if (x.classList.contains("clicked")){
                test++;
                downed = x.attributes["data-id"].nodeValue;
                button = x;
            }
        }
        return test;
    }

    // store game settings
    function setSetting (){
        if (box == "mode") {
            mode = curr;
        }else{
            diff = curr;
        }
    }

    // to depress button or to switch button
    if (notOnlyOne()){
        if (downed != curr){
            button.classList.remove("clicked")
            $(this).addClass("clicked");
            setSetting();
        }
    }else{
        $(this).addClass("clicked");
        setSetting();
    }
});

// clicking start
$("#start").click(function(){
    // check if options are valid
    let str = "";
    if (mode == "" || diff == ""){
        if (mode == "" && diff == ""){
            str = "game mode and difficulty";
        }else if (mode == ""){
            str = "game mode";
        }else{
            str = "difficulty";
        }

        swal({
            title: "Please select your options",
            text: "You didn't select the " + str, 
            icon: "error",
          });
    }else {
        let rules = $("#rules");
        let options = $("#options");
        let cArtist = $("#choose-artist");
        let cGenre = $("#choose-genre");
        
        // hide current windows and show 2nd option window
        options.hide();
        rules.hide();

        setTimeout(function () {
            if (mode == "artist"){
                cArtist.show();
            }else if (mode == "genre"){
                cGenre.show();
            }else {
                cArtist.show();
                cGenre.show();
            }
        }, 500);
    }
});

